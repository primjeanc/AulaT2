using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace MinhaBiblicoteca
{
    public class CalculoDeArea
    {
        /// <summary>
        /// Metodo para retornar a área de um quadrado
        /// </summary>
        /// <param name="ladoQuad">lado do quadrado</param>
        /// <returns></returns>
        public double CalculaAreaQuad(double ladoQuad)
        {
            return ladoQuad * ladoQuad;
        }
    }
}
